                <h2 class="align-center pb-3 mbr-fonts-style display-2">¿Que es el Peeling Químico?</h2>
                <h3 class="mbr-section-subtitle align-center mbr-light mbr-fonts-style display-7">El peeling químico es
                    un procedimiento de rejuvenecimiento de la piel en el que se le aplica una solución química con el
                    fin de eliminar las capas superiores dañadas. Las capas de piel reveladas tras realizar un peeling
                    químico son mas nuevas y por tanto más suaves y jóvenes.
                    <div>
                    </div>
                    ¿Para que se utiliza?<br>
                    <div><br></div>
                    <div>Los peelings químicos se utilizan para tratar las arrugas, eliminar manchas faciales y para las
                        cicatrices, sobre todo en la zona de la cara, donde tienen un efecto rejuvenecedor bastante
                        notable. La exfoliación química se puede hacer sola o en combinación con otros procedimientos
                        cosméticos.
                    </div>
                    <br>
                    <div>
                    </div>
                    <div>
                    </div>
                    <div>El tratamiento se puede realizar a diferentes profundidades (light, medio o profundo) en
                        función de los resultados deseados, lo que se consigue usando una solución química diferente
                        para cada caso. Los peelings químicos profundos tienen mejores resultados, pero también debemos
                        tener en cuenta que implican tiempos de recuperación más largos.
                    </div>
                </h3>

